// Ternary Operator

// Ternary operator is used to check conditions
// It is shortcut way, which can be used instead of if-else statements if needed


const blueTeamScore = 100
const redTeamScore = 220
let winningTeam

blueTeamScore > redTeamScore ? winningTeam = 'Blue' : winningTeam = 'Red'

console.log(`Game Winner: ${winningTeam} team`)